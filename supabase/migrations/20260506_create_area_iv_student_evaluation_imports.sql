create table if not exists public.area_iv_student_evaluation_imports (
  import_id bigint generated by default as identity not null,
  cycle_id bigint not null,
  employee_name text not null,
  normalized_name text not null,
  total_average_rate numeric(10, 2) not null default 0.00,
  matched_application_id bigint null,
  matched_faculty_id integer null,
  source_file_name text null,
  source_row_number integer null,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint area_iv_student_evaluation_imports_pkey primary key (import_id),
  constraint area_iv_student_evaluation_imports_cycle_fkey foreign key (cycle_id) references public.ranking_cycles (cycle_id) on delete cascade,
  constraint area_iv_student_evaluation_imports_application_fkey foreign key (matched_application_id) references public.applications (application_id) on delete set null,
  constraint area_iv_student_evaluation_imports_faculty_fkey foreign key (matched_faculty_id) references public.users (user_id) on delete set null,
  constraint area_iv_student_evaluation_imports_cycle_name_unique unique (cycle_id, normalized_name)
);

create index if not exists idx_area_iv_student_evaluation_imports_cycle_id on public.area_iv_student_evaluation_imports using btree (cycle_id);
create index if not exists idx_area_iv_student_evaluation_imports_normalized_name on public.area_iv_student_evaluation_imports using btree (normalized_name);
create index if not exists idx_area_iv_student_evaluation_imports_matched_application_id on public.area_iv_student_evaluation_imports using btree (matched_application_id);